package com.kspt.core.moxingku.datalist.conponent.pojo;

public class ButtonComponent {
	private String id;
	private String buttonName;
	private String impClass;
	private String buttonJavaScript;
	private String buttonPage;
	private String buttonGroup;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getButtonName() {
		return buttonName;
	}
	public void setButtonName(String buttonName) {
		this.buttonName = buttonName;
	}
	public String getButtonJavaScript() {
		return buttonJavaScript;
	}
	public void setButtonJavaScript(String buttonJavaScript) {
		this.buttonJavaScript = buttonJavaScript;
	}
	public String getButtonPage() {
		return buttonPage;
	}
	public void setButtonPage(String buttonPage) {
		this.buttonPage = buttonPage;
	}
	public String getButtonGroup() {
		return buttonGroup;
	}
	public void setButtonGroup(String buttonGroup) {
		this.buttonGroup = buttonGroup;
	}
	public String getImpClass() {
		return impClass;
	}
	public void setImpClass(String impClass) {
		this.impClass = impClass;
	}

}
